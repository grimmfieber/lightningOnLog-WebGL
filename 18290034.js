//Kütük objesini hazırlamak için.
function prepareLog() {
	for (var i = 0; i < quads.length; i+=12) {
        //Array indexi 1 den başladığı fakat gerçekte 0 dan başladığı için 1 azaltarak yazılır.
        addTriangleIndexVertices(quads[i]-1, quads[i+1]-1, quads[i+2]-1);
        addTriangleIndexVertices(quads[i+3]-1, quads[i+4]-1, quads[i+5]-1);
        addTriangleIndexVertices(quads[i+6]-1, quads[i+7]-1, quads[i+8]-1);

        addTriangleIndexVertices(quads[i]-1, quads[i+1]-1, quads[i+2]-1);
        addTriangleIndexVertices(quads[i+6]-1, quads[i+7]-1, quads[i+8]-1);
        addTriangleIndexVertices(quads[i+9]-1, quads[i+10]-1, quads[i+11]-1);
    }}

function addTriangleIndexVertices(vIndex, tIndex, nIndex){
    meshVertices.push(vertices[3*vIndex], vertices[3*vIndex+1], vertices[3*vIndex+2]);  //değerleri oluşturulan boş mesh arrayine dolduruyor.
    meshTexCoords.push(textureCoords[2*tIndex], textureCoords[2*tIndex+1]);
    meshNormals.push(normals[3*nIndex], normals[3*nIndex+1], normals[3*nIndex+2]);
}

function render() {

	gl.clear( gl.COLOR_BUFFER_BIT | gl.DEPTH_BUFFER_BIT);

    if(flag) theta[axis] += 0.5; //döndürme hızı

    modelView = mat4(); //hazır kütüphane fonksiyonu
    modelView = mult(modelView, rotate(theta[xAxis], [1, 0, 0] ));
    modelView = mult(modelView, rotate(theta[yAxis], [0, 1, 0] ));
    modelView = mult(modelView, rotate(theta[zAxis], [0, 0, 1] ));

    

	gl.uniformMatrix4fv( modelViewMatrixLoc, false, flatten(modelView));

   if(booleanCheck == true)  //Phong modeli için gl.TRIANGLES çizilecek.
   {
    gl.drawArrays(gl.TRIANGLES, 0, (meshVertices.length)/3 ); //mesh arrayinden çizdiriyor.
   }
    
    else  //wireframe yapısı için gl.LINES ile çizilecek
    {
    gl.drawArrays(gl.LINES, 0, meshVertices.length ); //mesh arrayinden çizdiriyor.
    }
    

	requestAnimFrame(render);
}